                        <p><b>
                                Circuito Sesc encanta São Roque com atrações culturais.</b>
                        </p>

                        <p>O evento cultural do Circuito Sesc de Artes proporcionou uma diversificada programação em São
                            Roque no último sábado, 25 de novembro. Originalmente programado para acontecer no C.E.C.T.
                            Brasital, o evento foi transferido para o centro cultural devido à previsão de chuvas. A
                            população desfrutou de um dia repleto de música, dança, teatro, cinema, artes visuais e
                            tecnologia.

                            O prefeito Guto Issa destacou a riqueza da programação cultural em São Roque, que encanta
                            não apenas os residentes locais, mas também toda a região. Ele agradeceu ao Sesc por trazer
                            novamente um festival vibrante e repleto de atividades para todas as idades. O prefeito
                            enfatizou que a cidade continua a se consolidar como um polo cultural na região.

                            O evento, realizado pelo Sesc São Paulo em colaboração com a Divisão de Cultura Municipal,
                            apresentou seis atrações distintas. Entre elas, o espetáculo teatral "Gigantes Modernistas",
                            encenado pelo grupo Pia Fraus, trouxe representações de artistas que trouxe representações
                            de alguns artistasque integraram o movimento modernista de 1922. Além disso, os amantes de
                            cinema tiveram a oportunidade de assistir ao curta-metragem "Amazônia Viva", disponibilizado
                            a todos com o uso de equipamentos de realidade virtual.
                            A música ficou por conta da DJ Simoníssima, de São Paulo, apresentando ritmos brasileiros, e
                            com as Pastoras do Rosário, um grupo coral formado por oito mulheres pretas e idosas, com um
                            repertório de sambas das décadas de 1990 e 2000.
                        </p>
