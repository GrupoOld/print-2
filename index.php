                    <p>A realização dessas melhorias foi principalmente atribuída à influência política do Sr. Antônio Joaquim da Rosa, também conhecido como o Barão de Piratininga. O Barão de Piratininga era uma figura importante em São Roque e aparentemente era um amigo do Imperador D. Pedro II. Em 1869, o Barão de Piratininga foi nomeado presidente da província de São Paulo.</p>
                    <p>A BRASITAL, uma das primeiras indústrias têxteis do Brasil, foi fundada por o industrial italiano Enrico Dell'Acqua em 1890 e continuou a funcionar até meados dos anos 1970. Um Centro Cultural, Educativo e Turístico, além da biblioteca municipal, agora faz parte do patrimônio público da cidade.</p>
                    <p>Três pioneiros - o lavrador José Casali, o engenheiro da Estrada de Ferro Sorocabana Dr. Eusébio Stevaux (francês) e Antônio dos Santos Sobrinho, também conhecido como Santinhos e nativo de São Roque - tentaram revitalizar a vitivinicultura em São Roque a partir de 1880. Embora apenas José Casali tenha se dedicado à vinicultura de forma comercial, todos os outros tinham seguidores. Embora as condições da região fossem ideais para o cultivo de vinhas, as técnicas de vinicultura eram obsoletas e careciam de apoio do governo. Do final do século XIX até a primeira década do século XX, houve um desenvolvimento gradual no cultivo da uva e na produção de vinho.</p>
